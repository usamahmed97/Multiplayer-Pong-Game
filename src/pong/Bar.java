//this is the rectangular bar model class
package pong;

public class Bar {

    
    int xx, yy;
    public Bar(int x, int y) {
    xx=x;
    yy=y;
    
    }

    public void setXx(int xx) {
        this.xx = xx;
    }

    public int getXx() {
        return xx;
    }

    public void setYy(int yy) {
        this.yy = yy;
    }

    public int getYy() {
        return yy;
    }
    
    
}
